number = int(input("Enter the day of the week (1-7): "))
if number == 1:
    print("\nMonday")
elif number == 2:
    print("\nTuesday")
elif number ==3:
    print("\nWednesday")
elif number == 4:
    print("\nThursday")
elif number == 5:
    print("\nFriday")
elif number == 6:
    print("\nSunday")
elif number == 7:
    print("\nSaturday")
else:
    print("Incorrect day of the week entered, try again.")